    private void txtTenKHActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtTenKHActionPerformed
        Date cvngay = (ngayLapHoaDon.getDate() == null ? null : Date.valueOf(dtf.format(ngayLapHoaDon.getDate())));
//        System.out.println(cvngay);
        dshoadon = daoHDB.timKiemHoaDon(txtMaHoaDon.getText(), txtTenKH.getText(), txtSDTKH.getText(), txtSĐTNV.getText(), cvngay);
        while (tableHoaDon.getRowCount() != 0) {
            modelHoaDon.removeRow(0);
        }
        themDuLieuHoaDonVaoBang(dshoadon);
    }//GEN-LAST:event_txtTenKHActionPerformed

    private void themDuLieuHoaDonVaoBang(ArrayList<HoaDonBan> dshoadon) {
        modelHoaDon = (DefaultTableModel) tableHoaDon.getModel();
        lblTongHoaDon.setText(String.valueOf(dshoadon.size()));

        for (HoaDonBan hdb : dshoadon) {
            modelHoaDon.addRow(new Object[]{
                hdb.getMaHoaDonBan(),
                hdb.getNgayLapHD(),
                hdb.getKh().getMaKH(),
                hdb.getKh().getTenKh(),
                hdb.getNv().getTenNV(),
                daoCTHD.giaTienTheoMaHD(hdb.getMaHoaDonBan())
            });
        }
    }

