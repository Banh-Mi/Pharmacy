    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        try {
            int index = tableHoaDon.getSelectedRow();
            if (index >= 0 && index < dshoadon.size()) {
                // Thuoc t= dsThemThuoc.get(index);
                int dialogButton = JOptionPane.YES_NO_OPTION;
                int dialogResult = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa thuốc này khỏi giỏ hàng", "Xác thực", dialogButton);
                if (dialogResult == 0) {
                    String maHD = dshoadon.get(index).getMaHoaDonBan();
                    System.out.println(maHD);
                    daoHDB.xoaHoaDon(maHD);
                }
                btnLamMoiActionPerformed(evt);
            } else {
                JOptionPane.showMessageDialog(this, "CHỌN DÒNG ĐỂ XÓA");
            }
        } catch (Exception e) {
            // TODO: handle exception
        }
    }//GEN-LAST:event_btnXoaActionPerformed

