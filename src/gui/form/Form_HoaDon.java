    private void btnTimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimActionPerformed

        Date cvngay = (ngayLapHoaDon.getDate() == null ? null : Date.valueOf(dtf.format(ngayLapHoaDon.getDate())));
//        System.out.println(cvngay);
        dshoadon = daoHDB.timKiemHoaDon(txtMaHoaDon.getText(), txtTenKH.getText(), txtSDTKH.getText(), txtSĐTNV.getText(), cvngay);
        while (tableHoaDon.getRowCount() != 0) {
            modelHoaDon.removeRow(0);
        }
        themDuLieuHoaDonVaoBang(dshoadon);
    }//GEN-LAST:event_btnTimActionPerformed

