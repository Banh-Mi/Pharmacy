    public boolean validateField() {
        String mkcu = new String(txtMKCrr.getPassword());
        String mkNew = new String(txtMKNew.getPassword());
        String mkNewXT = new String(txtMKNewXT.getPassword());
        if (mkcu.isEmpty() || mkNew.isEmpty() || mkNewXT.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Mật Khẩu không được để trống");
            return false;
        }

        if (mkcu.equals(mkNew)) {
            JOptionPane.showMessageDialog(this, "Mật Khẩu mới giống mật khẩu cũ");
            return false;
        }

        if (mkNew.equals(mkNewXT) == false) {
            JOptionPane.showMessageDialog(this, "Nhập Lại mật khẩu không trùng khớp");
            return false;
        }
        if (!mkNew.matches("^(?=.*[a-z])(?=.*[A-Z])(?=.*[0-9])(?=.*[!@#\\$%\\^&\\*]).{8,}$")) {
            JOptionPane.showMessageDialog(this, "Mật Khẩu mới phải:\n"
                    + "Có tối thiểu 8 ký tự\n"
                    + "Có ít nhất một kí tự viết thường (a-z)\n"
                    + "Có ít nhất một kí tự viết hoa (A-Z)\n"
                    + "Có ít nhất một chữ số (0-9)\n"
                    + "Có ít nhất một ký tự đặc biệt (!@#$%^&)");
            return false;
        }
        return true;
    }
