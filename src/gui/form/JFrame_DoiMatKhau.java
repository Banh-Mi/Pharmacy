    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed

        if (validateField()) {
            if (JOptionPane.showConfirmDialog(this, "Bạn chắc chắn muốn cập nhật không ?", "Cảnh báo",
                    JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                try {
                    String mk= new String (txtMKCrr.getPassword());
                    String tk= nhanVienDoiMK.getSdt();
                    NhanVien nvDoiMK = daonv.dangNhap(tk, mk);
                    if (nvDoiMK != null) {
                        String mkNew= new String (txtMKNew.getPassword());
                        daonv.capnhapMatKhau(mkNew, tk);
                        JOptionPane.showMessageDialog(this, "Cập nhật mật khẩu thành công!");
                    } else {
                        JOptionPane.showMessageDialog(this, "MẬT KHẨU SAI", "Thông báo lỗi", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (Exception ex) {
                    Logger.getLogger(Form_ThongTinCaNhan.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_btnCapNhatActionPerformed

